/* ---- query param helper ---- */
function getSlugFromQuery() {
  return new URLSearchParams(location.search).get("slug");
}

function findBySlug(list, slug) {
  return list.find((i) => i.slug === slug) || list[0];
}

/* ---- generic detail page renderer ----
   fields: { kicker, title, meta, synopsisTitle, synopsis, body2Title, body2, tags } */
function renderDetail(item, fields) {
  const set = (id, value, html) => {
    const el = document.getElementById(id);
    if (!el) return;
    if (html) el.innerHTML = value;
    else el.textContent = value;
  };

  set("detail-cover", coverBlockHTML(item), true);
  set("detail-kicker", fields.kicker || "");
  set("detail-title", pickText(item, "title", "titleEn"));
  set("detail-meta", fields.meta || "");
  set("synopsis-title", pickText(item, "synopsisTitle", "synopsisTitleEn") || "줄거리");
  set("synopsis-text", pickText(item, "synopsis", "synopsisEn") || "");
  set("body2-title", pickText(item, "body2Title", "body2TitleEn") || "");
  set("body2-text", pickText(item, "body2", "body2En") || "");

  const imageEl = document.getElementById("detail-image-block");
  if (imageEl) imageEl.innerHTML = coverBlockHTML(item);

  const tagsEl = document.getElementById("detail-tags");
  const tags = pickTags(item);
  if (tagsEl && tags.length) {
    tagsEl.innerHTML =
      '<div class="tag-row">' +
      tags.map((t) => '<div class="tag">#' + t + "</div>").join("") +
      "</div>";
  }

  document.title = pickText(item, "title", "titleEn") + " — JUJUBE BOOKS";
}

